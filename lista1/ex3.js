// Conta caracteres
function qtdCaracteres(texto) {
    return texto.length;
}

// Armazena o return da function dentro de uma variavel
resultado = qtdCaracteres("Disciplina de programação para web");

// Imprime essa variavel
console.log(`A quantidade de caracteres é: ${resultado}`);